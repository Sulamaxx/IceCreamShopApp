    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean selected = jRadioButton1.isSelected();
//        System.out.println(selected);
        boolean selected1 = jRadioButton2.isSelected();

        if (id_txt.getText().equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select required order", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (!selected && !selected1) {
            JOptionPane.showMessageDialog(this, "Please set required status", "warning", JOptionPane.WARNING_MESSAGE);
        } else {

            IceCreamOrder order = new IceCreamOrder();
            order.setId(Integer.parseInt(id_txt.getText()));
            order.setName(name_txt.getText());
            if (selected) {
                InPreparationState inPreparationState = new InPreparationState();
                boolean processOrder = inPreparationState.processOrder(order, Integer.parseInt(cid_txt.getText()));
                if (processOrder) {
                    JOptionPane.showMessageDialog(this, "Order update successfully", "success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Somethings went wrong! try agai later", "warning", JOptionPane.WARNING_MESSAGE);
                }

            } else if (selected1) {
                if (status_txt.getText().equals("Placed")) {
                    JOptionPane.showMessageDialog(this, "cannot access process please check the current status", "warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    OutForDeliveryState deliveryState = new OutForDeliveryState();
                    boolean processOrder = deliveryState.processOrder(order, Integer.parseInt(cid_txt.getText()));
                    if (processOrder) {
                        JOptionPane.showMessageDialog(this, "Order update successfully", "success", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Somethings went wrong! try agai later", "warning", JOptionPane.WARNING_MESSAGE);
                    }
                }
            }

            loadOrders(selectedValue);
            clearData();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

