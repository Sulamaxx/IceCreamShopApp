    public void loadOrders(int x) {

        try {
            ResultSet rs = null;
            switch (x) {
                case 1:
                    rs = MySQL.execute("SELECT * FROM `orders` INNER JOIN customers ON customers.customer_id=orders.customer_id ORDER BY order_date DESC");
                    break;
                case 2:
                    rs = MySQL.execute("SELECT * FROM `orders` INNER JOIN customers ON customers.customer_id=orders.customer_id WHERE status='Placed' ORDER BY order_date DESC");
                    break;
                case 3:
                    rs = MySQL.execute("SELECT * FROM `orders` INNER JOIN customers ON customers.customer_id=orders.customer_id WHERE status='InPreparation' ORDER BY order_date DESC");
                    break;
                case 4:
                    rs = MySQL.execute("SELECT * FROM `orders` INNER JOIN customers ON customers.customer_id=orders.customer_id WHERE status='OutForDelivery' ORDER BY order_date DESC");
                    break;
                default:
                    break;
            }

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("order_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("flavor"));
                v.add(rs.getString("toppings"));
                v.add(rs.getString("syrups"));
                v.add(rs.getString("quantity"));
                v.add(rs.getString("total_amount"));
                v.add(rs.getString("order_date"));
                v.add(rs.getString("customer_id"));
                v.add(rs.getString("customer_name"));
                v.add(rs.getString("status"));
                dtm.addRow(v);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            Logger.getLogger(OrderControl.class.getName()).log(Level.SEVERE, ex.getMessage(), ex);
        }

    }

